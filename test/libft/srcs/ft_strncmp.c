#include <libft.h>

int		ft_strncmp(char *s1, char *s2, size_t n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i])
	{
		n--;
		if (n == 0)
			return (0);
		if (s1[i] == '\0')
		return (0);
		i++;
	}
	return (s1[i] - s2[i]);
}
